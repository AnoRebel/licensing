#!/usr/bin/env node
// Rewrites every manifest in the monorepo to match ./VERSION.
//
// Covered:
//   - typescript/package.json      (the single TS package; npm target)
//   - typescript/jsr.json          (same package; jsr.io target)
//   - admin/package.json, examples/ts/package.json, tools/*/package.json
//   - licensing/version.go                   (single `Version` constant)
//
// Modes:
//   sync-versions.mjs           → rewrites files to match VERSION
//   sync-versions.mjs --check   → exits 1 if any file drifts (CI gate)

import { spawn } from 'node:child_process';
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const check = process.argv.includes('--check');

const version = (await readFile(join(repoRoot, 'VERSION'), 'utf8')).trim();
if (!/^\d+\.\d+\.\d+(-[\w.]+)?$/.test(version)) {
  console.error(`VERSION is not semver-shaped: ${version}`);
  process.exit(2);
}

const drifts = [];
const wrote = [];

async function rewriteJson(path, mutate) {
  const raw = await readFile(path, 'utf8');
  const obj = JSON.parse(raw);
  const before = JSON.stringify(obj);
  mutate(obj);
  const after = JSON.stringify(obj);
  if (before === after) return;
  if (check) {
    drifts.push(path);
    return;
  }
  const trailing = raw.endsWith('\n') ? '\n' : '';
  await writeFile(path, `${JSON.stringify(obj, null, 2)}${trailing}`);
  wrote.push(path);
}

// Explicit file list avoids depending on `fs.glob` (Node >= 22). Wildcard
// positions are resolved with `readdir`, which works on every supported Node.
const manifestFiles = [
  'typescript/package.json',
  'typescript/jsr.json',
  'admin/package.json',
  'examples/ts/package.json',
];
const toolsDir = join(repoRoot, 'tools');
const toolEntries = await readdir(toolsDir, { withFileTypes: true }).catch(() => []);
for (const entry of toolEntries) {
  if (entry.isDirectory()) manifestFiles.push(`tools/${entry.name}/package.json`);
}

for (const file of manifestFiles) {
  const full = join(repoRoot, file);
  await rewriteJson(full, (obj) => {
    if (obj.version !== undefined) obj.version = version;
  });
}

// Go: single-constant file, generated so pkg.go.dev surfaces the version.
const goVersionFile = join(repoRoot, 'licensing/version.go');
// The `x-release-please-version` annotation lets release-please rewrite the
// constant without running this script — it stays here so manual edits and
// the release-please generic updater agree on the exact file shape.
const goVersionBody = `// Code generated by release-please + scripts/sync-versions.mjs. DO NOT EDIT.
package licensing

// Version is the licensing module release, kept in sync with the root VERSION
// file so TypeScript packages and the Go module ship matching versions.
const Version = "${version}" // x-release-please-version
`;
const existingGo = await readFile(goVersionFile, 'utf8').catch(() => '');
if (existingGo !== goVersionBody) {
  if (check) {
    drifts.push(goVersionFile);
  } else {
    await writeFile(goVersionFile, goVersionBody);
    wrote.push(goVersionFile);
  }
}

if (check) {
  if (drifts.length > 0) {
    console.error(`VERSION drift detected in ${drifts.length} file(s):`);
    for (const p of drifts) console.error(`  ${p}`);
    console.error(`Run: node scripts/sync-versions.mjs`);
    process.exit(1);
  }
  console.log(`All manifests match VERSION=${version}`);
  process.exit(0);
}

if (wrote.length === 0) {
  console.log(`No changes; everything already at ${version}`);
} else {
  console.log(`Synced ${wrote.length} file(s) to version ${version}:`);
  for (const p of wrote) console.log(`  ${p}`);
}

// JSON.stringify(obj, null, 2) reflows arrays vertically. Biome's
// formatter wants short arrays inlined to match the rest of the
// monorepo, so we run `biome format --write` on the rewritten JSONs
// before exiting. Skipping this would force every release PR to
// commit a "fix biome lint" follow-up.
//
// We deliberately don't fail the sync on a missing biome — the
// script must work in tooling-light environments (release-please
// runs it without bun, for example). A non-zero biome exit is
// logged and ignored.
const jsonWrites = wrote.filter((p) => p.endsWith('.json'));
if (jsonWrites.length > 0) {
  await new Promise((resolve) => {
    const proc = spawn('bunx', ['biome', 'format', '--write', ...jsonWrites], {
      cwd: repoRoot,
      stdio: 'inherit',
    });
    proc.on('exit', (code) => {
      if (code !== 0) console.warn(`(biome format exited with code ${code}; ignoring)`);
      resolve();
    });
    proc.on('error', (err) => {
      console.warn(`(biome format unavailable: ${err.message}; ignoring)`);
      resolve();
    });
  });
}
